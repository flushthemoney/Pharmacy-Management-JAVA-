    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String userRole=(String) combo.getSelectedItem();
String Name=tName.getText();
SimpleDateFormat dFormat=new SimpleDateFormat("dd-MM-yy");
Date date=tDate.getDate();
String dob="";
if(date !=null){
    dob=dFormat.format(tDate.getDate());
    
}
String mobileNumber=tMobile.getText();
String email=tEmail.getText();
String address=tAddress.getText();
String username=tUserName.getText();
if(Name.equals("")){
JOptionPane.showMessageDialog(null,"Name is Required");}
else if(dob.equals("")){
JOptionPane.showMessageDialog(null, "Date Of birth is required");}
else if(mobileNumber.equals("")){
JOptionPane.showMessageDialog(null,"mobile number is required");}
else if(!mobileNumber.matches(mobileNumberPattern)){
JOptionPane.showMessageDialog(null,"moblie number field is invalid");}
else if(email.equals("")){
JOptionPane.showMessageDialog(null,"Email is Required");}
else if(!email.matches(emailPattern)){
JOptionPane.showMessageDialog(null,"email field is invalid");}
else if(username.equals("")){
JOptionPane.showMessageDialog(null,"username field is invalid");}
else if(address.equals("")){
JOptionPane.showMessageDialog(null,"Address is required");}
else{
try{
Connection con=ConnectionProvider.getCon();
PreparedStatement ps =con.prepareStatement("update appuser set userRole=?,Name=?,dob=?,mobileNumber=?,email=?,address=? where username=?");
ps.setString(1,userRole);
ps.setString(2,Name);
ps.setString(3,dob);
ps.setString(4,mobileNumber);
ps.setString(5,email);
ps.setString(6,address);
ps.setString(7,username);
ps.executeUpdate();
JOptionPane.showMessageDialog(null,"Updated Successfully");
setVisible(false);
new UpdateUser().setVisible(true);}

catch(Exception e){
JOptionPane.showMessageDialog(null,e);}}// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

