    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int checkUserExit = 0;
        String username = tUserName.getText();
if(username.equals("")){
JOptionPane.showMessageDialog(null,"Username is required");}
else{
    SimpleDateFormat dFormat=new SimpleDateFormat("dd-MM-yy");
try{
Connection con =ConnectionProvider.getCon();
Statement st=con.createStatement();
ResultSet rs=st.executeQuery("select *from appuser where username='"+username+"'");
while(rs.next()){
tUserName.setEditable(false);
checkUserExit=1;
tName.setText(rs.getString("Name"));
tMobile.setText(rs.getString("mobileNumber"));
tEmail.setText(rs.getString("email"));
tAddress.setText(rs.getString("address"));
String dob=rs.getString("dob");
if(dob !=""){
try{
tDate.setDate(dFormat.parse(dob));}
catch(Exception e){}}
if(rs.getString("userRole").equals("Admin")){
combo.removeAllItems();
combo.addItem("Admin");
combo.addItem("Pharmacist");}
else{
combo.removeAllItems();
combo.addItem("Pharmacist");
combo.addItem("Admin");}
}}

catch(Exception e){
JOptionPane.showMessageDialog(null,e);}
if(checkUserExit==0){
JOptionPane.showMessageDialog(null,"Username Doesn't exit");}}// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

